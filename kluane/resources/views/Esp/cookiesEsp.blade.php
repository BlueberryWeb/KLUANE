                   <p class="bold h6">¿Qué es la tecnología DNT?</p>
                   <p>
                    «Do Not Track» (DNT) es un concepto promovido por autoridades reguladoras estadounidenses, como la Comisión Federal de Comercio 
                    de EE. UU., para el sector de Internet con el objetivo de desarrollar e implementar un mecanismo que permita a los internautas 
                    controlar el seguimiento o rastreo que se realiza de sus actividades en línea en los distintos sitios web a través de la 
                    configuración de sus navegadores. El World Wide Web Consortium (W3C) lleva tiempo trabajando con agentes del sector, 
                    navegadores de Internet, empresas tecnológicas, entidades normativas para desarrollar un estándar de tecnología DNT. Aunque se 
                    han realizado algunos avances, han sido escasos. Hasta la fecha no se ha adoptado ninguna norma al respecto. Como tal, 
                    KLUANE DRILLING LTD  no responde a señales de «no rastreo».
